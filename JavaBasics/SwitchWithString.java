package JavaBasics;

public class SwitchWithString {
    public static void main(String[] args) {
        String name = "Ravi";

        switch(name) {
            case "Mohit":
                System.out.println("Matched...");
                break;
            case "Ravi":
                System.out.println("Matched...");
                break;
            case "Akash":
                System.out.println("Matched...");
                break;
            case "Vaibhav":
                System.out.println("Matched...");
                break;

        }
    }
}
